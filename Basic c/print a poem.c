#include <stdio.h>
int main(void)
{
    printf("Over hill, over dale,\n");
    printf("Thorough bush, thorough brier,\n");
    printf("Over park, over pale,\n");
    printf("Thorough flood, thorough fire!\n");
    printf("I do wander everywhere,\n");
    printf("Swifter than the moon's sphere;\n");
    printf("And I serve the Fairy Queen,\n");
    printf("To dew her orbs upon the green;\n");
    printf("The cowslips tall her pensioners be;\n");
    printf("In their gold coats spots you see;\n");
    printf("Those be rubies, fairy favours;\n");
    printf("In those freckles live their savours;\n");
    printf("I must go seek some dewdrops here,\n");
    printf("And hang a pearl in every cowslip's ear.\n");

    return 0;
}